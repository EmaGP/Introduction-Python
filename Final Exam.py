# Imprimir números pares: Escribe un programa que recorra los números del 1 al 20 e imprima solo los números pares.

# Calculadora simple: Crea una función que tome dos números y realice operaciones básicas (suma, resta, multiplicación, división) según la selección del usuario.

# Contador de palabras en una oración: Crea una función que cuente la cantidad de palabras en una oración ingresada por el usuario.

# Simulador de juego de dados: Crea un programa que simule un juego de dados. El usuario debe poder lanzar los dados y el programa debe mostrar los resultados y llevar un seguimiento del puntaje total.